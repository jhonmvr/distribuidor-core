package ec.com.def.pa.wrapper;

import java.util.Date;

public class AjusteWrapper {
	
	private Long porcentajeDeducible;
	private Long inversion;
	private Long produccion;
	private Long precioAjuste;
	private Long salvamento;
	private Long valorPerdida;
	private Long valorDeducible;
	private Long valorPagar;
	private Date fechaAjuste;
	private String observacionesAjuste;
	private String estado;
	public Long getPorcentajeDeducible() {
		return porcentajeDeducible;
	}
	public void setPorcentajeDeducible(Long porcentajeDeducible) {
		this.porcentajeDeducible = porcentajeDeducible;
	}
	public Long getInversion() {
		return inversion;
	}
	public void setInversion(Long inversion) {
		this.inversion = inversion;
	}
	public Long getProduccion() {
		return produccion;
	}
	public void setProduccion(Long produccion) {
		this.produccion = produccion;
	}
	public Long getPrecioAjuste() {
		return precioAjuste;
	}
	public void setPrecioAjuste(Long precioAjuste) {
		this.precioAjuste = precioAjuste;
	}
	public Long getSalvamento() {
		return salvamento;
	}
	public void setSalvamento(Long salvamento) {
		this.salvamento = salvamento;
	}
	public Long getValorPerdida() {
		return valorPerdida;
	}
	public void setValorPerdida(Long valorPerdida) {
		this.valorPerdida = valorPerdida;
	}
	public Long getValorDeducible() {
		return valorDeducible;
	}
	public void setValorDeducible(Long valorDeducible) {
		this.valorDeducible = valorDeducible;
	}
	public Long getValorPagar() {
		return valorPagar;
	}
	public void setValorPagar(Long valorPagar) {
		this.valorPagar = valorPagar;
	}
	public Date getFechaAjuste() {
		return fechaAjuste;
	}
	public void setFechaAjuste(Date fechaAjuste) {
		this.fechaAjuste = fechaAjuste;
	}
	public String getObservacionesAjuste() {
		return observacionesAjuste;
	}
	public void setObservacionesAjuste(String observacionesAjuste) {
		this.observacionesAjuste = observacionesAjuste;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}

	
}
